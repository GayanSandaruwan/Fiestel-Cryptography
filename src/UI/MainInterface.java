    private void chooseFileEnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseFileEnActionPerformed

        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            this.plainFile = jfc.getSelectedFile();
            this.plainFilePath = this.plainFile.getAbsolutePath();
            System.out.println(this.plainFilePath);
            this.plainFileName = this.plainFile.getName();

            this.filePathEn.setText(plainFilePath + "/" + this.plainFileName);
            this.filePathAlert.setText("Good");
        } else {
            this.plainFile = null;
        }
//        String text = readFile(plainFile);
//        String fileName = file.getName();
    }//GEN-LAST:event_chooseFileEnActionPerformed

    private void encryptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptBtnActionPerformed

        FiestelCipher cipher = new FiestelCipher();
        try {
            FileReader fs = new FileReader(plainFile);
            BufferedReader br = new BufferedReader(fs);
            long length = 0;
            String line;
            String text="";
            System.out.println("Reading File");
            while ((line = br.readLine()) != null) {
                text+=line;
                length += line.length();
            }
            System.out.println(line);
            int rounds = roundsCountEn.getSelectedIndex()+1;
            String key = encryptionKey.getText();
            System.out.println("Encryption Started");
            boolean encrypted=cipher.encrypt(rounds, key, text, this.plainFileName);
            if(encrypted){
                System.out.println("File Encryption Successfull Stored at"+plainFilePath+"\\"+plainFileName+"encrypted.txt");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(MainInterface.class.getName()).log(Level.SEVERE, null, ex);
            filePathAlert.setText("Selected File Is Incorrect");
        }


    }//GEN-LAST:event_encryptBtnActionPerformed
